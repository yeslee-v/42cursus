#include "ft_printf.h"
#include <stdio.h>

int	ft_printf(const char *str, ...)
{
	int s;
	va_list	ap;

	va_start(ap, str);
	printf("%s", str);	// %d
	//if (ft_strchr(str, '%'))

	s = va_arg(ap, int);
	printf("%d", s);
	va_end(ap);
	
	return (s);
}
