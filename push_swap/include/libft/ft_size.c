#include "libft.h"

size_t	ft_size(int n)
{
	size_t	cnt;

	cnt = 1;
	if (n < 0)
		n *= -1;
	while (n > 9)
	{
		n /= 10;
		cnt++;
	}
	return (cnt);
}
